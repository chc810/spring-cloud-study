package com.channelsoft.study.cloud.butelmanageservice.exception;

public class IllegalSourceServiceException extends ServiceException {

	/**
	 *
	 */
	private static final long serialVersionUID = 6093658367377425092L;

	public IllegalSourceServiceException() {
		super();
	}

	public IllegalSourceServiceException(String message) {
		super(message);
	}

	public IllegalSourceServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalSourceServiceException(Throwable cause) {
		super(cause);
	}
}
